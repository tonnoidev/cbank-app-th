package th.co.cbank.project.model;

import org.apache.log4j.Logger;

public class CalendarBean {
    private final Logger logger = Logger.getLogger(CalendarBean.class);
    private int year;
    private int month;
    private int day;

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }
}
